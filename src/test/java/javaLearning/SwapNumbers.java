package javaLearning;

public class SwapNumbers {

	public static void main(String[] args) {
		String a = "5";
		String b = "10";
		String output1 = a.replace("10", "");
		String output2 = b.replace("5", "");
		
		System.out.println("value of b = " + output1 );
		System.out.println("value of a = " + output2 );

	}

}
